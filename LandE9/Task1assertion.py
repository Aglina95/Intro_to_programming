def german_polite_form(name):
    """Puts 'Sehr geehrte Frau' to address a person properly in German."""
    return 'Sehr geehrte Frau ' + name

print(german_polite_form('Angelina'))

assert german_polite_form('Angelina') == 'Sehr geehrte Frau Angelina'
